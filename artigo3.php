        <br>
        <br>
        Um sistema de ar condicionado tem sua importância ligada, principalmente, ao bem estar dos ocupantes quanto ao conforto térmico. Em síntese, o princípio de funcionamento deste sistema baseia-se na mistura do ar que retorna dos ambientes climatizados com o ar externo, posterior filtração (para retirada de material particulado biológico ou não) e condicionamento térmico para ser novamente insuflado para os ambientes.
        <br>
        <br>
        A preocupação com os problemas oriundos da falta de manutenção destes sistemas começou a aparecer no âmbito internacional após a "Convenção dos Legionários", ocorrido na Pensilvânia - EUA, que culminou em inúmeras mortes e comprometimento da saúde de vários participantes. A causa de tal surto foi devido à presença de uma bactéria posteriormente denominada Legionella Pneumophila,que foi insuflada para o local onde ocorria o evento.
        <br>
        <br>
        Já no cenário nacional, esta preocupação veio à tona com a morte do Ministro das Comunicações, Sérgio Motta, acometido também pela legionelose. A partir de então o Ministério da Saúde estabeleceu normas de procedimentos e parâmetros para garantir a qualidade do ar dos ambientes climatizados, sendo estas a Portaria 3523, de agosto de 1998, a Resolução RE 176 de 24 de outubro de 2000 e RE 09 de 20 de janeiro de 2003.
        <br>
        <br>
        Estas normas regulamentam a periodicidade das limpezas dos componentes do sistema, os valores máximos de aceitação para fungos, CO², material particulado, velocidade do ar, umidade relativa e temperatura. Além disso é estabelecido que sistemas de climatização com capacidade acima de 5 TR (15.000 kcal/h=60.000 BTU/H),deverão manter um responsável técnico habilitado e implantar um PMOC (Plano de Manutenção, Operação e Controle).
        <br>
        <br>
        Outro fator importante a ser considerado é a inexistência de padrões nacionais máximos de aceitação e periodicidade para higienização de dutos. Estes parâmetros podem ser encontrados em normas internacionais como as de National Air Duct Cleaners Association (NADCA Standard 1992-01), que estabelece o limite máximo aceitável de 1 mg de poeira para cada 100 cm² de duto.

        <div class="subt&iacute;tuloInterno">Falta de manutenção e controle biológico</div>
        Um sistema de ar condicionado sem manutenção propicia um ambiente favorável para a proliferação de microrganismos, pode abrigar ainda insetos e, possivelmente até pequenos roedores, além da grande quantidade de material particulado ou mesmo dissipar eventuais odores.
        <br>
        <br>
        Os gastos acarretados pela falta de manutenção do sistema de climatização somado à ausência de controle microbiológico semestral, trazem conseqüências sobre a saúde dos ocupantes, podendo atingir grandes dimensões, uma vez que provoca o absenteísmo dos funcionários por motivos de saúde.
        <br>
        <br>
        Reparos nos sistema de dutos ou à casa de máquinas tornam-se muito mais dispendiosos em termos financeiros do que seria a manutenção periódica do sistema.

