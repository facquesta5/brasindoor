        - Dutos
        <br>
        <br>
        Superfície interna lisa com limites de estanqueidade, de acordo com a tabela 1. A cada 15 m deverão ser deixadas portas de inspeção que poderão ser eventualmente utilizadas para acesso do equipamento de limpeza. Dutos flexíveis de ligação a difusores com no máximo 2m de comprimentos recebendo especial atenção na montagem quanto aos detritos acumulados interiormente.

        <br>
        <br>
        Os dampers de regulagem preferencialmente com aletas aerodinâmicas, tampadas na lateral, para evitar acúmulo de detritos. Junto a eles, sempre porta de inspeção.
        Dampers corta fogo, sempre provido de porta de inspeção e aletas livres de materiais de fibras feita pela mão do homen (MMMF).

        <br>
        <br>
        Grelhas e difusores - todos os elementos terminais devem ser facilmente desmontáveis para inspeção, limpeza e desinfecção, inclusive do duto imediatamente anterior. Deverão ser tipo alta indução, permitindo rápida equalização da temperatura e velocidade na zona de ocupação, inferiores a 0,15 m/s.

        <br>
        <br>
        As grelhas de retorno, se localizadas em ambientes altamente geradores de poeiras ou outros aerodispersóides, deverão possuir tela e eventualmente filtro.

        <br>
        <br>
        Tomadas de ar exterior - deverão ser compostas de: veneziana, tela, damper, filtros com eficiência mínima G4 e eventualmente filtro de carvão ativado (especialmente em aplicações industriais com alto grau de contaminação ambiental). O uso de dois filtros F6 em série pode ser opção a considerar. Preferencialmente a pelo menos 3 m do solo e longe das torres de resfriamento( 10m), de sistemas de pulverização e de docas ou parques de estacionamento(7,5m).

        <br>
        <br>
        - Renovação do ar.
        <br>
        <br>
        Já se referiu anteriormente que o problema da renovação de ar deve ser rigorosamente calculado sendo que a porcentagem dor ar necessária por pessoas deve ser sempre garantida, atendendo às recomendações da Norma ASHRAE 62-2004. Esses valores, contudo, devem ser calculados.

        <br>
        <br>
        Considerando que o ar será sempre renovado apenas por diluição (definidos nos trabalhos de Rea-Ting Liu and All - Heating Piping and Air Conditioning, maio 1991) com 100% de ar exterior. Esses valores podem ser reduzidos a 75% ou até mesmo a 25% dependendo do grau de filtragem utilizado. Esse dado é muito importante em termos de economia de energia.

        <br>
        <br>
        Quando a instalação for de Volume de Ar Variável e se a variação de carga não for por números de pessoas (auditórios, teatros, salas de reunião), mas apenas carga térmica sensível, então o cálculo do valor mínimo a manter deve ser o da sala mais crítica para que os valores mínimos se mantenham.

        <br>
        <br>
        Alguns autores recomendam utilizar sensores de CO2 para controlar a quantidade de ar exterior. Mas, de novo a pergunta: onde colocar o sensor? Qual a sala crítica? Se os principais poluentes forem os COV, como reage o sistema?

        <br>
        <br>
        Mais recentemente há notícias de um sensor de gases misturados que detectam tabaco, fungos, poeiras, bactérias, gases e COV que poderá ser uma luz neste caminho.

        <br>
        <br>
        Os dados aqui apresentados pretendem ser uma contribuição à melhoria dos sistemas de ar condicionado, na procura de ambientes cada vez mais sadios e da melhor qualidade do ar.<br>
