            <li>Ventiladores com carca&ccedil;a desmont&aacute;vel, permitindo a limpeza interna do mesmo. Acionamento dos ventiladores por motor tipo rotor exterior, evitando polias e correias com seu desgaste e problemas inerentes;</li>
