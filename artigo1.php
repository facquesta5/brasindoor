            <li>Filtragem &ndash; recomenda-se dois est&aacute;gios de filtragem. Sendo o filtro de retorno de ar, um filtro plano classe G4 que receba classifica&ccedil;&atilde;o no teste colorim&eacute;trico com efici&ecirc;ncia: gravim&eacute;trica 80&lt;Em&lt;95 e colorim&eacute;trica&gt;30%, correspondente &agrave; classe B2 DIN 24185 parte 100 vers&atilde;o FEVEREIRO 1978;</li>
