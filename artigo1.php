        <div class="subtítuloInterno">Resumo</div>
        O sistema de ar condicionado é peça fundamental no processo de qualidade de ar interior em sistemas com condicionamento de ar.

        <br>
        <br>
        Ele pode ajudar a diminuir os efeitos de contaminação que adentram o sistema, ou se mal executado, potencializar esses mesmos efeitos. O trabalho apresentado percorre a problemática a ele associado para a obtenção de qualidade de ar em interiores.<br>
        <br>
        TRABALHO DE REVISÃO
        <br>
        Celso Cardoso Simões Alexandre
        <br>
        TROX Technik.

        <br>
        <br>
        É cada vez maior a preocupação com a qualidade do ar interior. Síndrome dos edifícios doentes é uma expressão em moda e as origens do problema estão razoavelmente identificadas e comentadas.

        Simplificando, podemos dizer que a qualidade do ar interior pode ser afetada por quatro grandes grupos responsáveis:<br>
        <div class="subt&iacute;tuloInterno">Grupo I - Contaminação interior</div>
        Concentração de formaldeído liberado por móveis, divisórias, carpetes, compostos orgânicos voláteis, agentes de limpeza, tabagismo, plantas e terra onde estão plantadas, águas nos vazos, ozônio resultante de motores elétricos, copiadoras, contaminação transportada para dentro do ambiente por pessoas, em suas roupas, objetos, etc.
        <br>
        <div class="subt&iacute;tuloInterno">Grupo II - Contaminação exterior</div>
        É a contaminação trazida ao ambiente em estudo pela necessidade de ventilação e renovação do ar interior pelo ar exterior. O ar exterior, dependendo de sua captação e condição, pode apresentar-se com teores significativos de: CO - monóxido de carbono, CO2 - dióxido de carbono, ozônio, NO2 - dióxido de nitrogênio, Pb - chumbo, fumaças em geral, particulados (menores que 10 micra).<br>
        <br>
        Referência especial se faz ao Radon (ou radônio) que aparece no interior de edifícios, através da infiltração por fissuras de paredes e chão, especialmente importantes em alguns países e locais.<br>
        <br>
        <div class="subt&iacute;tuloInterno">Grupo III - Contaminação no sistema de ar condicionado propriamente dito</div>
        O sistema de ar condicionado, sendo por si próprio originalmente "inerte", pode tornar-se, com o decorrer dos tempos, uma fonte importante de contaminação. Alguns pontos "fracos" do sistema que devem merecer atenção especial são:

        <br>
        <br>
        - Unidades de tratamento de ar
        Serpentinas de resfriamento e condensação, onde se podem localizar algas, fungos e poeiras...

        Bandejas de condensados, onde se pode localizar todos os poluentes anteriores. <br>
        <br>
        Nas bandejas, as quatro condições básicas para que os bioaerosóis proliferem, se encontram reunidas:<br>
        <ul>
            <li>Reservat&oacute;rio adequado (a pr&oacute;pria bandeja);</li>
            <li>Fonte de nutrientes (as poeiras e lodos que possam existir);</li>
            <li>Amplifica&ccedil;&atilde;o e crescimento dos microorganismos (poss&iacute;veis por haver &aacute;gua estagnada, nutriente e temperatura adequada);</li>
            <li>Dissemina&ccedil;&atilde;o e aerosoliza&ccedil;&atilde;o da &aacute;gua contendo os microorganismos (efetuada pela passagem do ar pela &aacute;gua da bandeja).<br>
            </li>
        </ul>
        - Dutos
        <br>
        <br>
        Desde a fabricação e montagem, tradicionalmente no Brasil feita em obra, os dutos apresentam um grau de sujeira elevado desde o "startup" da instalação. Mesmo que tenham sido originalmente limpos e uma vez que os sistemas de filtragem não têm eficiência de 100%, algumas partículas aparecem e ao fim de algum tempo, também existe um biofilme ,aí se encontrando fungos e outros microorganismos.

        <br>
        <br>
        - Distribuição de ar
        A introdução de quantidade adequada de ar em um ambiente, com taxas de ar exterior apropriadas não é garantia de que a efetividade da ventilação seja conseguida.
        <ul>
            <li>Curto-circuito de ar entre insuflamento e retorno;</li>
            <li>Divis&oacute;rias que impe&ccedil;am circula&ccedil;&atilde;o apropriada de ar;</li>
            <li>Difusores de ar n&atilde;o apropriados, ocasionando velocidade de ar acima dos valores recomendados, ((0,15 m/s) na zona de ocupa&ccedil;&atilde;o), podem ser a causa de um sistema com qualidade do ar inadequada.</li>
        </ul>
        - Renovação de ar e influência de um sistema VAV
        <br>
        <br>
        O estudo da renovação de ar é, desde logo, um ponto fraco em alguns sistemas. O que normalmente se faz é calcular a vazão de ar por sala e considerar como ar exterior a admitir na Tomada de Ar Exterior, a soma das necessidades individuais por sala, acontecendo que, por exemplo, numa sala interior, a carga sensível seja baixa, logo, o ar insuflado tenha um valor baixo e, no entanto, seja um auditório com muitas pessoas. O problema se agrava ainda mais, em sistemas de Volume de Ar Variável em regime de cargas parciais, se não forem tomadas as precauções adequadas.<br>
        <br>
        <div class="subt&iacute;tuloInterno">Grupo IV - Deficiências de um sistema global incorreto</div>

        <br>
        <p>Aqui se incluem as causas de qualidade de ar insatisfat&oacute;ria, devidas a:</p>
        <ol>
            <li>Insufici&ecirc;ncia de ar exterior (projeto deficiente);</li>
            <li>M&aacute; distribui&ccedil;&atilde;o de ar (localiza&ccedil;&atilde;o inadequada de difusores ou difusores de baixa qualidade e performance);</li>
            <li>Controle deficiente de temperatura (m&aacute; localiza&ccedil;&atilde;o dos sensores, BMS funcionando impropriamente);</li>
            <li>Localiza&ccedil;&atilde;o inadequada de tomada de ar exterior (junto &agrave; torre de resfriamento, ao n&iacute;vel da rua, onde a concentra&ccedil;&atilde;o de CO &eacute; elevada, em posi&ccedil;&atilde;o que receba ventos dominantes, polu&iacute;dos de terceiros ou da pr&oacute;pria f&aacute;brica, capta&ccedil;&otilde;es junto ao solo introduzir&atilde;o seguramente grandes quantidades de bact&eacute;rias, fungos, folhas, dejetos de p&aacute;ssaros e outros microorganismos perniciosos).</li>
        </ol>
        <p>As solu&ccedil;&otilde;es recomendadas para minimizar cada um dos grupos de respons&aacute;veis s&atilde;o basicamente os seguintes:</p>
        <p><strong>Grupo I</strong></p>
        <ol>
            <li>Elimina&ccedil;&atilde;o das fontes ou exaust&otilde;es localizadas;</li>
            <li>Ventila&ccedil;&atilde;o em n&iacute;veis adequados;</li>
        </ol>
        <p><strong>Grupo II</strong></p>
        <ol>
            <li>Capta&ccedil;&atilde;o adequada (se for o caso);</li>
            <li>Filtragem, inclusive via carv&atilde;o ativado se o ar exterior n&atilde;o atender &agrave;s condi&ccedil;&otilde;es necess&aacute;ria&nbsp; de pureza.</li>
        </ol>
        <p><strong>Grupo IV</strong></p>
        <ol>
            <li>Projeto adequado ou reprojeto, devido &agrave; modifica&ccedil;&otilde;es que podem ter sido introduzidas ap&oacute;s a execu&ccedil;&atilde;o do projeto original.</li>
        </ol>
        <p><strong>O Grupo III</strong> &eacute; a tarefa principal deste trabalho.<br>
            <br>
            - Unidades de tratamento de ar
            <br>
            <br>
            Estando em discussão o potencial cancerígeno das fibras minerais feitas pela mão do homem, "man made mineral fibers" (MMMF), e da dificuldade de limpeza do isolamento interior das unidades de tratamento de ar, seja do tipo "fan-coil", seja unidades "self contained" que ficarão seguramente contaminadas quando da limpeza das serpentinas, a primeira recomendação é:<br>
        <ul>
            <li>Recobrimento do isolamento com chapas galvanizadas, de alum&iacute;nio ou a&ccedil;o inox, independente da aplica&ccedil;&atilde;o de biocidas;<br>
            </li>
            <li>Pintura interna na cor branca, ep&oacute;xi, sendo as unidades providas de visores que permitam inspe&ccedil;&atilde;o visual;</li>
            <li>Unidades providas de ilumina&ccedil;&atilde;o interna, tipo tartaruga com grau de prote&ccedil;&atilde;o adequados contra jatos de &aacute;guas;</li>
            <li>Bandejas de recolhimento de condensados, obrigatoriamente em a&ccedil;o inoxid&aacute;vel, com fundo inclinado em liga&ccedil;&atilde;o de esgoto em seu ponto mais baixo, garantindo escoamento total da &aacute;gua;</li>
            <li>Sif&atilde;o, pelo menos com 20% de selo h&iacute;drico acima da press&atilde;o total do ventilador ( n&atilde;o da press&atilde;o de trabalho do ventilador ( uma vez que ao se sujarem os filtros a curva do sistema se altera e a press&atilde;o pode subir)</li>
            <li>Dreno cego adicional (DCA), permitindo esgotar a &aacute;gua que eventualmente se acumule por limpeza da unidade;( especialmente em unidades horizontais).</li>
            <li>Serpentinas com, no m&aacute;ximo, seis rows e 10 aletas por polegada. Maior n&uacute;mero de rows e de aletas por polegada dificulta a limpeza . Se um maior n&uacute;mero de rows for necess&aacute;rio, recomenda-se usar uma segunda serpentina separada da primeira, pelo menos 40cm a 60cm;BANDEJAS DE RECOLHIMENTO DE CONDENSADOS, UMA A CADA 1,2M</li>
